"""Evaluate the untuned Meta-Llama-3-8B-Instruct model on the same task.

Same pipeline as `eval_pipeline.py`, but the question is generated by the
**base** instruct model via its native chat template (not the
`### Question:` SFT template). This gives the base model a fair shot and
provides a comparison baseline for the SFT model.

Pipeline:
  1. Pick random producible targets from the hypergraph (same pool used for
     SFT eval; you can exclude training targets via --exclude_targets_from).
  2. Build a fresh solution trace per target.
  3. For each requested difficulty, prompt the base model via the chat
     template with the same DIFFICULTY_RUBRIC used in dataset generation.
  4. Ask the LLM judge (Claude or OpenAI via ``--llm-provider``) to score difficulty.
     of the requested difficulty).
  5. Save raw eval records to JSON for the metrics script.

Use ``--with-cot`` to prompt the base model to emit ``<reasoning>...</reasoning>``
before the question (same strip-before-judges behaviour as ``eval_pipeline.py``).
"""
import argparse
import json
import random
from pathlib import Path

import torch
from transformers import AutoModelForCausalLM, AutoTokenizer

from cot_utils import strip_reasoning_prefix
from judges import judge_difficulty, judge_faithfulness, judge_feasibility
from llm_client import add_llm_cli, describe_llm_client, llm_client_from_args
from prompts import DIFFICULTY_RUBRIC, format_trace
from traversal import HyperGraph

HERE = Path(__file__).parent
DATA = HERE / "data"


SYSTEM_GEN_BASE = f"""You are a physics-question writer. Given a solution
trace, a target variable, the required given variables, and a target
difficulty (1-10), write ONE physics word problem.

The problem MUST be solvable using ONLY the formulas in the trace, MUST use
all the given variables (with realistic numerical values), and MUST ask for
the target variable. Do NOT mention the difficulty number inside the
question text. Do NOT include the answer or solution steps.

Reply with ONLY the question text. No headings, no JSON, no markdown.

{DIFFICULTY_RUBRIC}"""


USER_GEN_BASE = """Solution trace (the only formulas the question should require):
{trace_str}

Target variable to ask for: {target}
Required given variables (must all appear, with realistic numerical values): {leafs}
Target difficulty: {difficulty}/10

Write the question now."""

USER_GEN_BASE_WITH_COT = USER_GEN_BASE + """

Follow the system message: write <reasoning>...</reasoning> first, then the
question text only (no JSON).

Write your reasoning and the question now."""

SYSTEM_GEN_BASE_WITH_COT = f"""You are a physics-question writer. Given a solution
trace, a target variable, the required given variables, and a target
difficulty (1-10), you produce ONE physics word problem.

Output format (strict order):
1. First output exactly one XML element <reasoning>...</reasoning>. Inside it,
   write concise chain-of-thought: how the trace links each leaf to the target
   through the formulas; what numerical value you assign to each leaf and why
   it is physically plausible; how you satisfy the difficulty rubric for this
   level without omitting any leaf; how the final sentence will ask for the
   target (or an allowed synonym). Keep this block under roughly 600 words.
2. Immediately after </reasoning>, output ONLY the word problem itself: plain
   prose, no headings, no JSON, no markdown. Do NOT mention the difficulty number
   inside the question. Do NOT include the answer or solution steps.

The problem MUST be solvable using ONLY the formulas in the trace, MUST use
all the given variables (with realistic numerical values), and MUST ask for
the target variable.

{DIFFICULTY_RUBRIC}"""


def load_base(base_model: str):
    tok = AutoTokenizer.from_pretrained(base_model)
    if tok.pad_token is None:
        tok.pad_token = tok.eos_token
    tok.padding_side = "left"
    model = AutoModelForCausalLM.from_pretrained(
        base_model, torch_dtype=torch.bfloat16, device_map="auto"
    )
    model.eval()
    return model, tok


@torch.no_grad()
def generate_question(
    model,
    tok,
    trace_str: str,
    target: str,
    leafs,
    difficulty: int,
    max_new_tokens: int = 768,
    *,
    with_cot: bool = False,
) -> str:
    leaf_str = ", ".join(leafs) if leafs else "(none)"
    sys_c = SYSTEM_GEN_BASE_WITH_COT if with_cot else SYSTEM_GEN_BASE
    usr_t = USER_GEN_BASE_WITH_COT if with_cot else USER_GEN_BASE
    messages = [
        {"role": "system", "content": sys_c},
        {"role": "user", "content": usr_t.format(
            trace_str=trace_str, target=target, leafs=leaf_str,
            difficulty=difficulty)},
    ]
    prompt_ids = tok.apply_chat_template(
        messages, add_generation_prompt=True, return_tensors="pt"
    ).to(model.device)
    out = model.generate(
        prompt_ids, max_new_tokens=max_new_tokens, do_sample=True,
        temperature=0.8, top_p=0.95, pad_token_id=tok.eos_token_id,
    )
    text = tok.decode(out[0][prompt_ids.shape[1]:], skip_special_tokens=True)
    return text.strip()


def tokens_for_difficulty(d: int, base: int, scale: int) -> int:
    """Easy questions are short; difficulty-10 narratives are long."""
    return base + scale * max(0, d - 3)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--graph", default=str(DATA / "physics_hypergraph.json"))
    ap.add_argument("--base_model", default="meta-llama/Meta-Llama-3-8B-Instruct")
    ap.add_argument("--output", default=str(DATA / "eval_base_results.json"))
    ap.add_argument("--num_targets", type=int, default=40)
    ap.add_argument(
        "--difficulties",
        type=int,
        nargs="+",
        default=list(range(1, 11)),
        help="Requested difficulty levels per target (default: 1..10).",
    )
    ap.add_argument("--max_depth", type=int, default=5)
    ap.add_argument("--seed", type=int, default=42)
    ap.add_argument("--exclude_targets_from", default=str(DATA / "dataset.json"),
                    help="JSON file whose `target` values are skipped.")
    ap.add_argument("--gen_tokens_base", type=int, default=350,
                    help="max_new_tokens at difficulty<=3")
    ap.add_argument("--gen_tokens_scale", type=int, default=80,
                    help="extra max_new_tokens per difficulty step above 3")
    ap.add_argument(
        "--with-cot",
        action="store_true",
        help="Ask the base model for <reasoning>...</reasoning> then the question; "
        "strip the reasoning block before Claude judges (fair comparison to CoT SFT).",
    )
    ap.add_argument("--single_domain", action="store_true", default=True,
                    help="restrict every eval trace to one physics chapter "
                         "and subdomain (must match dataset generation)")
    ap.add_argument("--no_single_domain", dest="single_domain",
                    action="store_false")
    add_llm_cli(ap)
    args = ap.parse_args()

    rng = random.Random(args.seed)
    g = HyperGraph(args.graph)
    candidates = g.producible()
    excluded = set()
    if Path(args.exclude_targets_from).exists():
        with open(args.exclude_targets_from) as f:
            excluded = {it["target"] for it in json.load(f)}
    pool = [c for c in candidates if c not in excluded] or candidates

    model, tok = load_base(args.base_model)
    judge = llm_client_from_args(args)
    judge_desc = describe_llm_client(judge)
    print(f"Judge backend: {judge_desc}", flush=True)

    out = []
    out_path = Path(args.output)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    meta_path = out_path.with_suffix(".meta.json")
    with open(meta_path, "w", encoding="utf-8") as mf:
        json.dump(
            {
                "llm_provider": args.llm_provider,
                "llm_model": judge.model,
                "judge_backend": judge_desc,
                "base_model": args.base_model,
            },
            mf,
            indent=2,
        )
    i, attempts = 0, 0
    while i < args.num_targets and attempts < args.num_targets * 5:
        attempts += 1
        target = rng.choice(pool)
        trace = g.traverse(target, max_depth=args.max_depth,
                           seed=rng.randint(0, 10**9),
                           single_domain=args.single_domain)
        if trace is None or not trace["path"]:
            continue
        if args.single_domain and (len(trace["chapters"]) != 1
                                   or len(trace.get("subdomains", [])) > 1):
            continue
        i += 1
        trace_str = format_trace(trace)
        chapters = ",".join(trace["chapters"])
        subs = ",".join(trace.get("subdomains", trace["chapters"]))
        print(f"[{i}/{args.num_targets}] target={target} "
              f"chapter={chapters} sub={subs} steps={len(trace['path'])}",
              flush=True)
        for d in args.difficulties:
            mnt = tokens_for_difficulty(d, args.gen_tokens_base,
                                        args.gen_tokens_scale)
            if args.with_cot:
                mnt += 512
            raw_out = generate_question(
                model, tok, trace_str, target, trace["leafs"], d,
                max_new_tokens=mnt,
                with_cot=args.with_cot,
            )
            question = strip_reasoning_prefix(raw_out)
            claude_diff = judge_difficulty(
                judge, trace_str, trace["leafs"], target, question)
            claude_faith = judge_faithfulness(
                judge, trace_str, trace["leafs"], target, question)
            claude_feas = judge_feasibility(
                judge, trace_str, trace["leafs"], target, question)
            out.append({
                "target": target,
                "trace": {
                    "leafs": trace["leafs"],
                    "chapters": trace["chapters"],
                    "hypernym": trace["hypernym"],
                    "path": [{"id": e["id"], "output": e["output"],
                              "inputs": e["inputs"], "label": e["label"],
                              "domain": e["domain"]} for e in trace["path"]],
                },
                "trace_str": trace_str,
                "requested_difficulty": d,
                "claude_difficulty": claude_diff,
                "claude_faithfulness": claude_faith,
                "claude_feasibility": claude_feas,
                "question": question,
                "model": "base",
            })
            print(f"  d={d} requested={d} claude={claude_diff} "
                  f"faithful={claude_faith['faithful']} "
                  f"coverage={claude_faith['variable_coverage']:.2f} "
                  f"feasibility={claude_feas}",
                  flush=True)
            tmp = out_path.with_suffix(".json.tmp")
            with open(tmp, "w") as f:
                json.dump(out, f, indent=2)
            tmp.replace(args.output)

    print(f"\nSaved {len(out)} base-model eval records to {args.output}")


if __name__ == "__main__":
    main()
